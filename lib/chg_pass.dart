  @override
  _ChangePasswordPageState createState() => _ChangePasswordPageState();
}

class _ChangePasswordPageState extends State<ChangePasswordPage> {
  final _formKey = GlobalKey<FormState>();
  final _currentUser = FirebaseAuth.instance.currentUser;

  final _oldPasswordController = TextEditingController();
  final _newPasswordController = TextEditingController();
  final _confirmPasswordController = TextEditingController();

  bool _isLoading = false;

  @override
  void dispose() {
    _oldPasswordController.dispose();
    _newPasswordController.dispose();
    _confirmPasswordController.dispose();
    super.dispose();
  }

  Future<void> _tryChangePassword() async {
    setState(() {
      _isLoading = true;
    });

    if (!_formKey.currentState!.validate()) {
      setState(() {
        _isLoading = false;
      });
      return;
    }

    if (_newPasswordController.text != _confirmPasswordController.text) {
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(content: Text('Passwords do not match')),
      );
      setState(() {
        _isLoading = false;
      });
      return;
    }

    if (_currentUser == null || _currentUser.email == null) {
      throw Exception('Current user or email is null');
    }

    final credential = EmailAuthProvider.credential(
      email: _currentUser.email!,
      password: _oldPasswordController.text,
    );

    try {
      await _currentUser.reauthenticateWithCredential(credential);
      await _currentUser.updatePassword(_newPasswordController.text);

      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(content: Text('Password changed successfully')),
      );
      // Navigate to a secure landing page or handle success as needed
    } on FirebaseAuthException catch (e) {
      String message = 'An error occurred: ${e.code}';
      switch (e.code) {
        case 'weak-password':
          message =
              'The password provided is too weak. Please choose a stronger password.';
          break;
        case 'wrong-password':
          message = 'The old password is incorrect. Please try again.';
          // Add a snackbar to inform the user about the incorrect old password
          ScaffoldMessenger.of(context).showSnackBar(
            const SnackBar(content: Text('Old password is incorrect')),
          );
          break;
        case 'network-request-failed':
          message =
              'No internet connection available. Please check your connection and try again.';
          break;
        default:
          message = 'An error occurred. Try again later.';
          break;
      }
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text(message)),
      );
    } finally {
      setState(() {
        _isLoading = false;
      });
    }
  }

